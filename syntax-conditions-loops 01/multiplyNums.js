function multiplyNumber(numbers) {
  let num1 = numbers[0];
  let num2 = numbers[1];

  console.log(num1 * num2);
}

multiplyNumber([2, 3]);

function multiplyOrDivide(nums) {
  let num1 = numbers[0];
  let num2 = numbers[1];

  if (num1 <= num2) {
    console.log(num1 * num2);
  } else {
    console.log(num1 / num2);
  }
}

multiplyOrDivide([2, 3]);

